  `SCHEDULE_DATE` DATE NOT NULL COMMENT '日期',
  `SCHEDULE_WORKING_STAFF` VARCHAR(320) NOT NULL COMMENT '上班員工',
  `SCHEDULE_DAYOFF_STAFF` VARCHAR(10) NOT NULL COMMENT '休假員工',

  PRIMARY KEY (`SCHEDULE_DATE`))
COMMENT = '班表';

