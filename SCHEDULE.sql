-- 創建資料庫
CREATE DATABASE `YOKULT`;

USE `YOKULT`;

-- 會員資料
DROP TABLE IF EXISTS `SCHEDULE`;

CREATE TABLE `SCHEDULE` (
  `SCHEDULE_DATE` VARCHAR(50) NOT NULL COMMENT '日期',
  `SCHEDULE_WORKUNG_STAFF` VARCHAR(320) NOT NULL COMMENT '上班員工',
  `SCHEDULE_DAYOFF_STAFF` VARCHAR(10) NOT NULL COMMENT '休假員工',

  PRIMARY KEY (`SCHEDULE_DATE`))
COMMENT = '排班';

